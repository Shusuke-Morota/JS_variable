const a = {
    prop: 0
}

const b = {
    prop: 0
}

// ここでオブジェクトa,bが同じかどうか確認してみる。

console.log(a === b);

// この結果はfalseとなる。
// このようにオブジェクトを比較する場合には、
// 変数a,bに入っているのは、あくまで、object aの参照になるため、
// この場合だとオブジェクトの参照を確認して、参照先のオブジェクトが変わってくるため、falseが返ってくる。
// このためオブジェクトを比較する場合は、オブジェクトの中にある　プロパティを比較する必要がある。

console.log(a.prop === b.prop);

// この場合だと、オブジェクトのプロパティが参照している値を持ってくるため、trueが返ってくる。


// 結論
// プリミティブ型では値の比較が行われる。
// オブジェクトは参照の比較が行われる。
